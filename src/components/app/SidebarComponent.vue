            }

            return [];
        },
    },

    methods: {
        isNavActive(item) {
            return this.$route.name == item.name;
        },

        navItemClass(item) {
            if(this.isNavActive(item)) {
                return 'bg-violet-50 text-violet-700 shadow-sm ring-1 ring-violet-100';
            }

            return 'text-neutral-700 hover:bg-neutral-100 hover:text-neutral-950';
        },

        navIconClass(item) {
            if(this.isNavActive(item)) {
                return 'bg-violet-100 text-violet-700';
            }

            return 'bg-neutral-100 text-neutral-600 group-hover:bg-white group-hover:text-neutral-900';
        },

        sidebarComponentClick() {
            if(this.$global.isClickDropdown.userSetting) 
                this.$global.isClickDropdown.userSetting = false;
        },
    }
